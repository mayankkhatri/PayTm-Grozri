// *? Updating Cart Numbers

function onLoadCartNumbers() {
  let productNumbers = localStorage.getItem("cartNumbers");
  let totalSpan = document.querySelector(".bskt-cost");
  let cartCost = localStorage.getItem("totalCost");

  if (productNumbers) {
    document.querySelector(".cart-span").textContent = productNumbers;

    document.querySelector(".go-cart").style.visibility = "visible";

    totalSpan.textContent = cartCost;
  }
  if (cartCost == 0) {
    document.querySelector(".go-cart").style.visibility = "hidden";
  }
  if (productNumbers >= 9) {
    document.querySelector(".cart-span").style.left = "16.8px";
  }
}
function cartNumbers(product, action) {
  let productNumbers = localStorage.getItem("cartNumbers");
  let cartSpan = document.querySelector(".cart-span").textContent;

  productNumbers = parseInt(productNumbers);
  cartSpan = parseInt(cartSpan);
  if (action) {
    localStorage.setItem("cartNumbers", productNumbers - 1);
    document.querySelector(".cart-span").textContent = productNumbers - 1;
    console.log("action running");
  } else if (productNumbers) {
    localStorage.setItem("cartNumbers", productNumbers + 1);
    document.querySelector(".cart-span").textContent = productNumbers + 1;
  } else {
    localStorage.setItem("cartNumbers", 1);
    document.querySelector(".cart-span").textContent = 1;
  }

  if (cartSpan >= 9) {
    document.querySelector(".cart-span").style.left = "16.8px";
  }

  setItems(product);
}

function popUpdate() {
  let totalSpan = document.querySelector(".bskt-cost");
  let cartCost = localStorage.getItem("totalCost");
  document.querySelector(".go-cart").style.visibility = "visible";
  document.querySelector(".go-cart").classList.add("fade-in-only");
  totalSpan.textContent = cartCost;
  if (cartCost == 0) {
    document.querySelector(".go-cart").style.visibility = "hidden";
  }
}

function setItems(product) {
  let cartItems = localStorage.getItem("productsInCart");
  cartItems = JSON.parse(cartItems);

  if (cartItems != null) {
    if (cartItems[product.tag] == undefined) {
      cartItems = {
        ...cartItems,
        [product.tag]: product,
      };
    }
    cartItems[product.tag].incart += 1;
  } else {
    product.incart = 1;
    cartItems = {
      [product.tag]: product,
    };
  }

  localStorage.setItem("productsInCart", JSON.stringify(cartItems));
}

function totalCost(product, action) {
  let cart = localStorage.getItem("totalCost");
  let cartCost = localStorage.getItem("totalCost");

  if (action) {
    cart = parseInt(cart);

    localStorage.setItem("totalCost", cart - product.price);
  } else if (cartCost != null) {
    cartCost = parseInt(cart);
    localStorage.setItem("totalCost", cartCost + product.price);
  } else {
    localStorage.setItem("totalCost", product.price);
  }
}
